    public void TampilCustomer(){
        Object header[] = {"Kode Customer","Nama Customer","Email"};
        DefaultTableModel data = new DefaultTableModel(null, header);
        List<Customer> listCustomer = new ArrayList<>();
        listCustomer = customerCtrl.findCustomerEntities();
        if(listCustomer.size() == 0){
            tblCustomer.setModel(data);
        }else{
            for(int i = 0; i < listCustomer.size(); i++){
                customer = listCustomer.get(i);
                String row[] = {customer.getKodecustomer(), customer.getNamacustomer(), customer.getEmail()};
                data.addRow(row);
                tblCustomer.setModel(data);
            }
        }
    }
    
