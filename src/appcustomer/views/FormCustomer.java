    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        // TODO add your handling code here:
        //GetModel
        DefaultTableModel model = (DefaultTableModel)tblCustomer.getModel();
       
        //Get selected row index
        int selectedRowIndex = tblCustomer.getSelectedRow();
        
        //Set the selected row data into jtextfield
        txtKodeCustomer.setText(model.getValueAt(selectedRowIndex, 0).toString());
        txtNamaCustomer.setText(model.getValueAt(selectedRowIndex, 1).toString());
        txtEmail.setText(model.getValueAt(selectedRowIndex, 2).toString());
    }//GEN-LAST:event_tblCustomerMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        customer.setKodecustomer(txtKodeCustomer.getText());
        customer.setNamacustomer(txtNamaCustomer.getText());
        customer.setEmail(txtEmail.getText());
        
        try {
            customerCtrl.create(customer);
            JOptionPane.showMessageDialog(this, "Data Tersimpan");
            ClearTextBox();
            TampilCustomer();
        } catch (Exception ex) {
            Logger.getLogger(FormCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

